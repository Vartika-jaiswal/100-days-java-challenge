package condition_and_loops;

public class typecasting {
    public static void main(String[] args) {
        // typecasting
        int num= (int)(67.46f);
        System.out.println(num);
        // automatic type promotion in expressions
        int a=257;
        byte b=(byte)(a);
        System.out.println(b); //257%256=1
        


    }
}
